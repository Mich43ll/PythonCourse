import math

print(round(1.5)) #redondea el decimal
print(abs(-77)) # regresa el valor absoluto
print(math.ceil(1.1)) #regresa el numero entero superior mas cercano
print(math.floor(1.1)) #regresa el numero entero inferior mas cercano
print(math.isnan(23)) # nos dice si el valor entre parentesis NO es un numero
print(math.pow(10, 3)) #eleva a la potencia del segundo parametro
print(math.sqrt(12)) #regresa la raiz cuadrada